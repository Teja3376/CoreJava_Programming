package com.studenttribe.corejava.nestedloops;

public class Loops {

	public static void main(String[] args) {
		//For loop statement
		int i ;
		for(i=1;i<10;i++) {
			System.out.println("JAVA");
		}
		//While loop statement
		int j ;
		j = 1;
		while(j<=10) {
			{
				System.out.println("Program");
				j++;
			}
		}
	}

}

 
 
